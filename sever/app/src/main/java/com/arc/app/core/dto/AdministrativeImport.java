package com.arc.app.core.dto;

public class AdministrativeImport {
    private String codeProvince;
    private String nameProvince;
    private String codeDistrict;
    private String nameDistrict;
    private String codeCommune;
    private String nameCommune;

    public String getCodeProvince() {
        return codeProvince;
    }

    public void setCodeProvince(String codeProvince) {
        this.codeProvince = codeProvince;
    }

    public String getNameProvince() {
        return nameProvince;
    }

    public void setNameProvince(String nameProvince) {
        this.nameProvince = nameProvince;
    }

    public String getCodeDistrict() {
        return codeDistrict;
    }

    public void setCodeDistrict(String codeDistrict) {
        this.codeDistrict = codeDistrict;
    }

    public String getNameDistrict() {
        return nameDistrict;
    }

    public void setNameDistrict(String nameDistrict) {
        this.nameDistrict = nameDistrict;
    }

    public String getCodeCommune() {
        return codeCommune;
    }

    public void setCodeCommune(String codeCommune) {
        this.codeCommune = codeCommune;
    }

    public String getNameCommune() {
        return nameCommune;
    }

    public void setNameCommune(String nameCommune) {
        this.nameCommune = nameCommune;
    }
}
